                  <div class="post-entry">
                    <p><b><h4>Current State</h4></b>As stated earlier for Perpetual license, the 
                    software that can be free for life is converted into a pay-and-use style subscription.
                    Theoretically, If one buys a PC and uses it for two years but later either the PC sold to some other
                    or broken, he paid the software only for two years of usage not for its life. More or less like 
                    renting an apartment of condominium where you rent a fully-furnished home for a fee. Use it as 
                    provided and not to own it. 
                    </p>
                    <p>
                    The onus is on the owner of the software to keep pace with the changing environment. 
                    It is the service providers responsibility to create the comfort and ward-off the threats 
                    that increase with time.
                    There cannot be any reluctance to update or resistance to change because service seeker is paying for it.
                    One can see this model every where eg. buying flight ticket to renting a taxi on street. 
                    Pay and use the service. Once the service period is over leave it without attachments or emotions.
                    </p>
                    <p><b><h4>Definition</h4></b>
                    Pay a small fee for using the software as a service, nothing more. 
                    </p>
                    <p></p>
                    <blockquote>
                      <p>Excellence is never an accident. It is always the result of high intention, 
                      sincere effort, and intelligent execution; it represents the wise choice 
                      of many alternatives - choice, not chance, determines your destiny.
                      --Aristotle</p>
                    </blockquote>
                    <p><b><h5>Pros</h5></b> As with everything there are pros and cons. Let's examine them.</p>
                    <ul>
                      <li>No need for huge upfront investment.</li>
                      <li>Pay for what you use either number of employees or a fixed full scale service fee.</li>
                      <li>Absolutely no need for any hardware to buy or install. It is on cloud, you can access anywhere on earth!</li>
                      <li>Budgeting of service is so easy to do.</li>

                    </ul>
                    
                    <p><b><h5>Cons</h5></b></p>
                    <ul>
                      <li>Good quality software costs good subscription fee. Evaluate or use the free period to judge to avoid heart burn later on.</li>
                      <li>Estimation of needs is an important exercise as service provider caters needs as sudden changes are not serviceable.
                      </li>
                      <li>Fixed flows and certain changes may incur cost but it is true for perpetual also.</li>
                    </ul>
                    
                    <p></p>


                    <p><b><h4>Decision</h4></b>
                      Subscription model takes out a huge volume of effort from top decision makes as no need for 
                      investment on infrastructure or its associated personnel. Simply choose the service provider to your
                      satisfaction. If not satisfied, walk away from them. One can save money and effort. 
                    </p>
                    
                    <p><b><h4>Choice</h4></b>
                      Smaller organizations or upcoming entities cannot afford to invest in infrastucture
                      which is needless. One should ideally opt for subscription based service and live with
                      peace of mid and yet still achieve the goals of the organization. 
                      One piece of advice: Take a trial run, be satisfied, no need to compromise. 
                      Choose a product that is ready, much like to buy a ready to move-in home.
                      </p>
 

