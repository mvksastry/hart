                  <div class="post-entry">
                    <p><b><h4>Current State</h4></b>Since when computers and software began to evovle, a one-time
                    purchase of a PC used to come with software that it works perpetual or eternal.
                    However, nothing in the universe works perpetual except planet rotaion! For everything
                    else, there is a limit, so does software. Except a few software categories such as computers
                    that operate equipment, everthing else has to keep pace with the changing environment.
                    </p>
                    <p>
                    In order to keep pace with the changing environment, comfort and threats increase with time and so does the need to update.
                    Any reluctance to update or resistance to change may result in damages that might not be easy
                    to recover, both interms of time and effort. Moreover, such resistance to change may result in
                    heavy loss of valuable data which can threaten the very existence of entities. 
                    </p>
                    <p><b><h4>Definition</h4></b>
                    Perpetual licensing is when a software vendor sells a license to a user for a one-time fee. 
                    </p>
                    <p>Usually, annual maintenance contracts are needed to implement changes and dedicated personnel to keep track changes
                    in the code implemented. Often newer versions or branches are not covered and everything needed is fixed for life.
                    As long as the original installation is running nothing to change and it will continue to give the service.</p>
                    <blockquote>
                      <p>Excellence is never an accident. It is always the result of high intention, 
                      sincere effort, and intelligent execution; it represents the wise choice 
                      of many alternatives - choice, not chance, determines your destiny.
                      --Aristotle</p>
                    </blockquote>
                    <p><b><h5>Pros</h5></b> As with everything there are pros and cons. Let's examine them.</p>
                    <ul>
                      <li>It involves a one time fee and user can have as long as it performs.</li>
                      <li>If your entity is not going to grow in employees or work flow dramatically, this is the best choice</li>
                      <li>For a given hardware, with annual maintenance contract, one can get going.</li>
                    </ul>
                    
                    <p><b><h5>Cons</h5></b></p>
                    <ul>
                      <li>It can go out-dated much sooner than anticipated.</li>
                      <li>Growth of organization can affect the outcome and performance of the product
                      </li>
                      <li>No matter with any amount of AMC, it is insufficient to keep pace with change.</li>
                    </ul>
                    
                    <p>The European languages are members of the same family. Their separate existence is a myth. For science, music, sport, etc, Europe uses the same vocabulary. The languages only differ in their grammar, their pronunciation and their most common words.</p>


                    <p><b><h4>Decision</h4></b>
                      An office that is growing with number or people or work-flows is not a static entity, no matter
                      how simple the work is. Perpetual license for an office is not a good decision unless no intention of growing. 
                    </p>
                    
                    <p><b><h4>Choice</h4></b>
                      Investing high amount of money upfront for product that goes outdated sooner is never a good choice.
                      It is better to leave pepetual license and go for subscription based license for growing organizations.
                       
                    </p>
 

